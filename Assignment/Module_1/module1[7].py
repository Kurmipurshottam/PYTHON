'''
7] Write a Python program to find whether a given number is even or odd print out an appropriate
message to the user.
'''
no=int(input("Enter that number you want to check even or odd = "))
if no%2==0:
    print(" : : entered number is an Even number : : ")
elif no==0:
    print(" : : entered number is zero : : ")
else:
    print(" : : entered number is an Odd number : :")
