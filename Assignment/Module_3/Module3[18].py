'''
18] What happens when „1‟== 1 is executed ?
 ->  When the expression "1" == 1 is executed in Python, it will evaluate to False.
       This is because the equality (==) operator compares the values on both sides
       of the operator for equality, and in this case, the left side is a string ("1") and
       the right side is an integer (1).
'''
# Comparing a string to an integer using the equality operator (==)
result = "1" == 1
# Printing the result of the comparison
print(result)

 

